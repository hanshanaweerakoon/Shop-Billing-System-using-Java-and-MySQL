    public void function() {
        String date = t.getText();

        try {
            jLabel4.setText("TO " + date);
            pst = con.prepareStatement("SELECT Code,Name,Remark,Total FROM billhistory WHERE DateTime='" + date + "' AND Cash= 1");
            rs = pst.executeQuery();
            table1.setModel(DbUtils.resultSetToTableModel(rs));

            pst = con.prepareStatement("SELECT Code,Name,Remark,Total FROM stockhistory WHERE DateTime='" + date + "' AND Cash= 1");
            rs = pst.executeQuery();
            table2.setModel(DbUtils.resultSetToTableModel(rs));
            table1.setEnabled(false);
            table2.setEnabled(false);

            getTotal(table1, total1);
            getTotal(table2, total2);

            Double t1 = Double.parseDouble(total1.getText());
            Double t2 = Double.parseDouble(total2.getText());

            if (t1 >= t2) {

                balance1.setText(Double.toString(t1 - t2));
                balance2.setText("0");
            } else {

                balance2.setText(Double.toString(t2 - t1));
                balance1.setText("0");
            }
            balance1.setEditable(false);
            balance2.setEditable(false);

        } catch (Exception e) {
        }

    }

    public void getTotal(JTable table, JTextField total) {

        double sum = 0;
        for (int i = 0; i < table.getRowCount(); i++) {

            sum += Double.parseDouble(table.getValueAt(i, 3).toString());

        }
        total.setText(Double.toString(sum));
        total.setEditable(false);

    }

