    public Double debit(String tablename) {

        try {
            pst = con.prepareStatement("SELECT SUM(DebitRs) FROM " + tablename);
            rs = pst.executeQuery();
            rs.next();
            return rs.getDouble("SUM(DebitRs)");

        } catch (Exception e) {
            return 0.0;
        }
    }
    
    
    public Double credit(String tablename) {

        try {
            pst = con.prepareStatement("SELECT SUM(CreditRs) FROM " + tablename);
            rs = pst.executeQuery();
            rs.next();
            return rs.getDouble("SUM(CreditRs)");

        } catch (Exception e) {
            return 0.0;
        }
    }
    
    
